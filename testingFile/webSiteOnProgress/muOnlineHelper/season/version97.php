			<h4>Gameplay</h4>
			<p>
				MU Online was created in December 2001 by the Korean gaming company WEBZEN Inc. Like most MMORPGs, 
				players have to create a character among nine different classes and to set their foot on the MU Continent. 
				In order to gain experience and thus to level up, a players needs to fight monsters (mobs). 
				MU is populated by a large variety of monsters, from simple ones like goblins and golems, 
				to frightening ones such as the Gorgon, Kundun or Selupan. Each monster-type is unique, 
				has different spawn points, and drops different items. Outside of a duel, PvP fighting is possible but discouraged. 
				When one player attacks another, a self-defense system is activated, 
				in which the attacked player may kill the aggressor within a time limit. 
				Self-defense is also activated when a player attacks an Elf's summoned creature.
				 Outlaws have disadvantages during game play, depending on their Outlaw level.
			</p>
			<h4>Character classes</h4>
			<p>
				At the start of the game the players can choose between three different character classes - 
				Dark Wizard, Dark Knight and Fairy Elf. Players can unlock two classes if they reach certain 
				level with one character: Magic Gladiator, and Dark Lord. Also, character cards can be purchased 
				in order to unlock more character classes. Each class has its specific powers and items. As characters 
				level up and complete quests, they can transform themselves into stronger classes. Each progressive
				class change grants, gain access to new spells, skills,
				weapons and wings as well as an associated change in appearance.
			</p>
			<h5>Spells and Skills</h5>
			<p>
				Like most MMO games, characters in MU can use many different kinds of magic and special abilities.
				Each character has its own set of spells and some weapons may be enchanted in order to provide the character with a specific spell. 
				Casting a spell costs MP (mana, or magic point) and sometimes AG or stamina.
			</p>
			<h5>Communications</h5>
			<p>
				Mu Online has a built-in chat system that allows all visible players to easily communicate via text. Users can add custom players to 
				their Friends List in-order to make it easy to keep in contact with one-another. Other methods of chat were available as well, such as Guild chat and Party chat.
			</p>
			<h5>Cooperative Play</h5>
			<p>
				Players can choose to team up with other players in a "party", in which players cooperate against enemy monsters to share exp and loot. 
				Early seasons of the game encouraged party play by rewarding the player with the highest level more experience points for helping lower 
				level players. Players could not see the level of other players in the party but because spending character points on vitality was highly 
				discouraged, players could guess quite accurately the level of other players by seeing their health bars. Some builds were made for parties 
				only like the part elf, who focused on filling up the energy stat, being protected by a summoned mob, and buffing party members.
			</p>
