			<h4>Mountain hiking from Bansko to the Seven Rila Lakes</h4><hr>
		
			<p>The Seven Rila Lakes are a group of glacial lakes, situated in the northwestern Rila Mountains in Bulgaria. They are the most visited group of lakes in Bulgaria. The lakes are situated between 2,100 and 2,500 metres elevation above sea level.

Each lake carries a name associated with its most characteristic feature.

-The highest one is called Salzata (“The Tear”) due to its clear waters that allow visibility in depth.

-The next one in height carries the name Okoto (“The Eye”) after its almost perfectly oval form. Okoto is the deepest  cirque lake in Bulgaria, with a depth of 37.5 m.

-The lake with steepest shores of the entire group is called Babreka (“The Kidney”).

-The largest one by area is called Bliznaka (“The Twin”).

-Trilistnika (“The Trefoil”) has an irregular shape and low shores.

-The shallowest lake is Ribnoto Ezero (“The Fish Lake”).

-The lowest one is Dolnoto Ezero (“The Lower Lake”), where the waters that flow out of the other lakes are gathered to form the Dzherman River.

The Seven Lakes are a tourist attraction in Bulgaria because of its natural environment. The lakes are located one above the other and are connected by small streams, which form tiny waterfalls and cascades. Tourist accommodation in the lakes’ vicinity include a chalet on the northeastern shore of The Fish Lake, at an elevation of 2,196 m. The most common time to visit the lakes is summer, in July and August, when temperature is above 10 degrees Celsius and the risk of sudden storms is lower. During the rest of the year the weather can be severe. The lakes usually freeze during October, and do not melt before June. The ice cover can reach up to 2 meters.

Transport from Bansko to Pionerska Hut by car for 2 hours, then on the chair lift up to the Seven Rila Lakes Hut. The walk starts from the Rila Lakes Hut and goes around the Lake Valley, and then back down to the hut. The way back to Bansko is the same by car and chair lift. The transport and the walk around the lakes take the whole day.
The tour is hard level.</p><hr>
