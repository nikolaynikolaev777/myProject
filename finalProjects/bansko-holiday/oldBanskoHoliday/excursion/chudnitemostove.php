			<h4>Chudnite Mostove (The Wonderful Bridges)</h4><hr>
		
			<p>Chudnite Mostove (The Wonderful Bridges) is a rock phenomenon also known as the Skalnite Mostove (The Rock Bridges). It is situated in the karst valley of the river Erkyupriya in the West Rhodope mountain on 1450 above the sea level, at the foot of Golyam Persenk peak (2091). The rock phenomenon is situated at a distance of 80 km south from Plovdiv, 35 km north-west from Chepelare and only 5 km north-west from the village of Zabardo. Since 1949 this is a protected natural monument. In 1961 they were declared a natural landmark and in 2003 were included as Number 85 in the list of the One Hundred National Tourists Sites of the Bulgarian Tourist Union.

In the past the bridges had been one entire cave, formed under the destructive activity of the river waters. Parts of it were collapsing with time, forming majestic marble bridges. The large bridge is about 15 meters wide in its wider parts and almost 100 meters long. It consists of three arches, as the largest one is 45 meters high and 40 meters wide. The small bridge is at a distance of 200 meters from the large one along the river current. It is impassable, 60 meters long, with total height of 50 meters, and the height of the arch is 30 meters. There is also a really small third bridge after it, which is a ponor cave, in which the waters of Erkyupriya River disappear, to appear again on the surface 3 kilometers farther. The famous Rhodopean Rose (Haberlea rodopenzis) grows on these natural bridges.

The two large bridges are safeguarded and accommodated for the touristic needs. Passing is allowed over and under them.

A great variety of animal species inhabits the surrounding forests. About 270 bird species can be seen in the area, as well as bears, wolves, wild boar, rabbits, foxes, mountain goats, deer, stags. The Alpian Newt (salamander) and some bat species are included in the Red Book.

This part of the Rhodopes is rich in herbst, mushrooms and wild fruits. A lot of rare and endemic species are included in the list of protected plants: Rhodopean Viola (Viola rhodopeia), Rhodopean Lilium (Lilium rhodopeum), Red Rhodopean Tulip (Tulipa rhodopea), Rhodopean Geum (Geum rhodopeum), Persian Morina (morina persica), Veronica rhodopaea, Lathrea and Haberlea rhodopensis, also known as the Orphean Flower.

The Wonderful Bridges can be reached through a third-class road, starting from the Asenovgrad-Smolan main road and passing by the village of Zabardo. Well marked hiking trails connect the area to the village of Zabardo, the Kabata Chalet, the Rock Bridges Chalet, the Persenk Chalet and the Izgrev Chalet.</p><hr>
