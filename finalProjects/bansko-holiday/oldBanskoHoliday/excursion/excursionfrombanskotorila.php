			<h4>Excursion from Bansko to Rila monastery</h4><hr>
		
			<p>The Monastery of Saint Ivan of Rila, better known as the Rila Monastery is the largest and most famous Eastern Orthodox monastery in Bulgaria. It is situated in the southwestern Rila Mountains, 117 km south of the capital Sofia in the deep valley of the Rilska River. The monastery is named after its founder, the hermit Ivan of Rila (876 – 946 AD).

The monastery complex, regarded as one of the foremost masterpieces of Bulgarian National Revival architecture, was declared a national historical monument in 1976 and became a UNESCO World Heritage Site in 1983. Since 1991 it has been entirely subordinate to the Holy Synod of the Bulgarian Orthodox Church.

The whole complex occupies an area of 8,800 sq.m and is rectangular in form, centred around the inner yard 3,200 sq.m, where the tower and the main church are situated.

The museum of the Rila Monastery is particularly famous for housing Rafail’s Cross, a wooden cross made from a whole piece of wood.</p><hr>
